#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	unsigned char		*dest;
	const unsigned char	*source;

	source = src;
	dest = dst;
	if (!dst && !src)
		return (0);
	while (n--)
		*dest++ = *source++;
	return (dst);
}
