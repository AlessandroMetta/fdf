#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		n;

	n = ft_strlen(s);
	while (n)
	{
		if (s[n] == (char)c)
			return ((char *)(s + n));
		n--;
	}
	if (s[n] == (char)c)
		return ((char *)(s + n));
	return (NULL);
}
