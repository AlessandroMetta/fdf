#include "libft.h"

void	*ft_memccpy(void *dst, const void *src, int c, size_t n)
{
	unsigned char		*dest;
	const unsigned char	*source;

	source = src;
	dest = dst;
	while (n--)
	{
		*dest++ = *source++;
		if ((unsigned char)c == *(source - 1))
			return (dest);
	}
	return (NULL);
}
